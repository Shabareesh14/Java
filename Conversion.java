class Conversion{
		byte age;
		int number;
		short digit;
		float num;
		long liters;
		double value;
		boolean identity;
		char string;
	public static void main(String[] args){
		
		Conversion convert = new Conversion();
		System.out.println(convert.age);
		System.out.println(convert.number);
		System.out.println(convert.digit);
		System.out.println(convert.num);
		System.out.println(convert.value);
		System.out.println(convert.liters);
		System.out.println(convert.identity);
		System.out.println(convert.string);
	}
}